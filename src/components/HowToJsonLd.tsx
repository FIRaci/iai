interface HowToStep {
  name: string
  text: string
}

export function HowToJsonLd({ name, description, steps }: { name: string; description: string; steps: HowToStep[] }) {
  const json = {
    "@context": "https://schema.org",
    "@type": "HowTo",
    name,
    description,
    step: steps.map((s, i) => ({
      "@type": "HowToStep",
      position: i + 1,
      name: s.name,
      text: s.text,
    })),
  }

  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: JSON.stringify(json) }}
    />
  )
}
